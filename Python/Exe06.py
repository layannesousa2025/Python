# amarração de um inteiro a uma variavel
idade = 30
print("Idade:",idade) #Exibe: Idade:30

# Amarração de uma string a mesma variavel
idade ="Trinta"
print("Idade em palavras:",idade)
# Exibe:Idade em palavra:trinta

# Amarração dde um float a uma nova variavel
altura = 1.70
print("Altura:",altura)
#Exibe: Altura: 1.75